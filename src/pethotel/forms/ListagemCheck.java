      private void preencherTabela() {
            
        CheckDAO checkDAO = new CheckDAO();

        List<Check> listaChecks = checkDAO.getCheck();

        DefaultTableModel tabelaChecks = (DefaultTableModel) tblCheck.getModel();
        
       
        tblCheck.setRowSorter(new TableRowSorter(tabelaChecks));

        for (Check c : listaChecks) { 
            Object[] obj = new Object[] { 

                c.getId(),
                c.getNomePet(),
                c.getDataEntrada(),
                c.getDataSaida(),
                c.getDiarias(),
                c.getValor(),
                c.getObs()
             
            };
            tabelaChecks.addRow(obj);
        }    
    }
      
      
    private void preencherTabelaPesquisa(String nome) {
            
        CheckDAO checkDAO = new CheckDAO();

        List<Check> listaChecks = checkDAO.getCheckP(nome);

        DefaultTableModel tabelaChecks = (DefaultTableModel) tblCheck.getModel();

        tabelaChecks.setRowCount(0);

        tblCheck.setRowSorter(new TableRowSorter(tabelaChecks));

        for (Check c : listaChecks) { 
            Object[] obj = new Object[] { 

                c.getId(),
                c.getNomePet(),
                c.getDataEntrada(),
                c.getDataSaida(),
                c.getDiarias(),
                c.getValor(),
                c.getObs()
            };

            tabelaChecks.addRow(obj);
        }    
    }


