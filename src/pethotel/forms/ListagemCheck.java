    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel tabeloModelo = (DefaultTableModel) tblCheck.getModel();
        int row = tblCheck.getSelectedRow();
        int id = (Integer) tabeloModelo.getValueAt(row, 0);
        
        CheckDAO checkDAO = new CheckDAO();
        
        String[] options = { "Sim", "Não" };
        
            int deletar = JOptionPane.showOptionDialog(
                        rootPane,
                        "Tem certeza que deseja excluir?", 
                        "Exclusão de hospedagem", 
                        JOptionPane.DEFAULT_OPTION, 
                        JOptionPane.QUESTION_MESSAGE, 
                        null, 
                        options, 
                        options[0]
                 );

                if(deletar == 0){

                    checkDAO.excluir(id);

                    JOptionPane.showMessageDialog(rootPane,"Hospedagem excluida com sucesso");
                   
                    ((DefaultTableModel) tblCheck.getModel()).setRowCount(0);
                
                    preencherTabela();
                                  
                }
            
                else{
                    tblCheck.clearSelection();             
                    }
        
        
    }//GEN-LAST:event_btnExcluirActionPerformed

